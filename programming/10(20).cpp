#include <iostream>

void divide_numbers(double a, double b) {
  if (a != 0) {
    std::cout << b / a << std::endl;
  } else {
    std::cout << "Cannot divide by zero." << std::endl;
  }
}

int main() {
  divide_numbers(2.0, 8.0); // Output: 4
  divide_numbers(0.0, 8.0); // Output: Cannot divide by zero.
  return 0;
}

